using System;

class Program
{
    static void Main()
    {
        // Задаем цену кристаллов
        int цена_кристаллов = 10;

        // Получаем начальное количество золота от пользователя
        Console.Write("Введите начальное количество золота: ");
        int начальное_золото = Convert.ToInt32(Console.ReadLine());

        // Предлагаем пользователю купить кристаллы и получаем количество кристаллов
        Console.Write("Сколько кристаллов вы хотите купить? ");
        int количество_кристаллов = Convert.ToInt32(Console.ReadLine());

        // Вычисляем количество кристаллов, которые пользователь может купить
        int доступное_количество_кристаллов = начальное_золото / цена_кристаллов;

        // Проверяем, возможна ли сделка
        if (количество_кристаллов <= доступное_количество_кристаллов)
        {
            // Вычисляем остаток золота и кристаллов после сделки
            int остаток_золота = начальное_золото - количество_кристаллов * цена_кристаллов;
            int общее_количество_кристаллов = количество_кристаллов;

            // Выводим результаты на экран
            Console.WriteLine($"Остаток золота: {остаток_золота}");
            Console.WriteLine($"Количество кристаллов: {общее_количество_кристаллов}");
        }
        else
        {
            // Выводим актуальные данные перед сделкой
            Console.WriteLine($"Недостаточно золота для покупки {количество_кристаллов} кристаллов.");
            Console.WriteLine($"Доступно кристаллов для покупки: {доступное_количество_кристаллов}");
            Console.WriteLine($"Остаток золота: {начальное_золото}");
        }
    }
}