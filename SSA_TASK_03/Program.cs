using System;

class Program
{
    static void Main()
    {
        // Характеристики босса
        int жизниБосса = 500;
        int уронБосса = 50;

        // Характеристики игрока
        int жизниИгрока = 500;
        bool вРазломеИлиНет = false;

        // Возможные заклинания
        int рашамонУрон = 100;
        int хуганзакураУрон = 100;
        int восстановлениеХП = 250;

        // Битва с боссом
        while (жизниБосса > 0 && жизниИгрока > 0)
        {
            // Выводим текущие характеристики
            Console.WriteLine($"У игрока {жизниИгрока} HP и у босса {жизниБосса} HP");

            // Игрок выбирает заклинание
            Console.WriteLine("Выберите заклинание:");
            Console.WriteLine("1. Рашамон");
            Console.WriteLine("2. Хуганзакура");
            Console.WriteLine("3. Межпространственный разлом");
            Console.WriteLine("4. Колдовской огонь");
            Console.WriteLine("5. Ледяной удар");

            int выбор = Convert.ToInt32(Console.ReadLine());

            // Применяем выбранное заклинание
            switch (выбор)
            {
                case 1:
                    if (!вРазломеИлиНет)
                    {
                        жизниИгрока -= уронБосса; // Игрок получает урон от босса
                        жизниИгрока -= рашамонУрон; // Игрок использует заклинание
                    }
                    else
                    {
                        Console.WriteLine("Вы не можете использовать Рашамон в разломе!");
                    }
                    break;
                case 2:
                    if (вРазломеИлиНет)
                    {
                        жизниБосса -= хуганзакураУрон; // Игрок использует заклинание
                    }
                    else
                    {
                        Console.WriteLine("Хуганзакура может быть выполнен только после призыва теневого духа!");
                    }
                    break;
                case 3:
                    if (!вРазломеИлиНет)
                    {
                        жизниИгрока += восстановлениеХП; // Игрок восстанавливает HP
                        вРазломеИлиНет = true; // Игрок скрывается в разломе
                    }
                    else
                    {
                        Console.WriteLine("Вы уже находитесь в разломе!");
                    }
                    break;
                case 4:
                    if (!вРазломеИлиНет)
                    {
                        жизниБосса -= 2 * рашамонУрон; // Игрок использует заклинание
                    }
                    else
                    {
                        Console.WriteLine("Вы не можете использовать Колдовской огонь в разломе!");
                    }
                    break;
                case 5:
                    if (!вРазломеИлиНет)
                    {
                        жизниБосса -= хуганзакураУрон / 2; // Игрок использует заклинание
                    }
                    else
                    {
                        Console.WriteLine("Вы не можете использовать Ледяной удар в разломе!");
                    }
                    break;
                default:
                    Console.WriteLine("Неверный выбор.");
                    break;
            }

            // Босс атакует
            if (!вРазломеИлиНет)
            {
                жизниИгрока -= уронБосса;
            }
        }

        // Выводим результат битвы
        if (жизниБосса <= 0)
        {
            Console.WriteLine("Вы победили босса! Поздравляем!");
        }
        else
        {
            Console.WriteLine("Босс победил вас. Попробуйте еще раз!");
        }
    }
}